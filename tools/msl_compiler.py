from msl_parser import MSLParser, SetTempo, Instrument
from msl_codegen import CodeGenerator

class MSLCompiler:
    def __init__(self):
        self.parser = MSLParser()
        self.generator = CodeGenerator()

    def compile(self, song_name: str, channel_a_mml: str, channel_b_mml: str, channel_c_mml: str) -> str:
        # 1. Parse each channel
        events_a = self.parser.parse(channel_a_mml)
        events_b = self.parser.parse(channel_b_mml)
        events_c = self.parser.parse(channel_c_mml)

        # 2. Separate instruments
        all_events = events_a + events_b + events_c
        instruments = [e for e in all_events if isinstance(e, Instrument)]
        events_a = [e for e in events_a if not isinstance(e, Instrument)]
        events_b = [e for e in events_b if not isinstance(e, Instrument)]
        events_c = [e for e in events_c if not isinstance(e, Instrument)]

        # Find BPM
        bpm_step = "#12C0" # default
        for event in all_events:
            if isinstance(event, SetTempo):
                bpm_step = f"#{event.bpm_step:04X}"
                break
        
        # Remove tempo events from channels
        events_a = [e for e in events_a if not isinstance(e, SetTempo)]
        events_b = [e for e in events_b if not isinstance(e, SetTempo)]
        events_c = [e for e in events_c if not isinstance(e, SetTempo)]

        # 3. Generate the full Z80 file content
        z80_code = f"""; -- {song_name} - Generated by MSLCompiler --

"""
        
        # --- Generate header ---
        header_label = f"{song_name.upper()}_HDR"
        cha_label = f"{song_name.upper()}_CHA"
        chb_label = f"{song_name.upper()}_CHB"
        chc_label = f"{song_name.upper()}_CHC"
        inst_ptr = "0"
        if instruments:
            inst_ptr = "INST_TBL"

        z80_code += f"{header_label}:\n"
        z80_code += f"    DEFW    {bpm_step}, {cha_label}\n"
        z80_code += f"    DEFW    {bpm_step}, {chb_label}\n"
        z80_code += f"    DEFW    {bpm_step}, {chc_label}\n"
        z80_code += f"    DEFW    {inst_ptr}\n\n"

        # --- Generate instruments ---
        if instruments:
            z80_code += self.generator.generate_instruments(instruments)
            z80_code += "\n"
        
        # --- Generate channel A ---
        z80_code += f"; --- CHANNEL A ---\n"
        z80_code += f"{cha_label}:\n"
        z80_code += self.generator.generate(events_a)
        
        # --- Generate channel B ---
        z80_code += f"; --- CHANNEL B ---\n"
        z80_code += f"{chb_label}:\n"
        z80_code += self.generator.generate(events_b)

        # --- Generate channel C ---
        z80_code += f"; --- CHANNEL C ---\n"
        z80_code += f"{chc_label}:\n"
        z80_code += self.generator.generate(events_c)

        return z80_code

def main():
    compiler = MSLCompiler()
    
    # Example MML for 3 channels
    mml_a = """
    @INST(0, "VibratoLead") {
        ADSR: 10, 5, 255, 10
        LFO: 1, 0, 2, 12, 20
        FLAGS: 0
    }
    L4 C E G @I0
    """
    mml_b = "L4 E G B"
    mml_c = "L4 G B > D"
    
    z80_file_content = compiler.compile("MY_SONG", mml_a, mml_b, mml_c)
    
    print("--- Generated Z80 File ---")
    print(z80_file_content)

if __name__ == "__main__":
    main()
